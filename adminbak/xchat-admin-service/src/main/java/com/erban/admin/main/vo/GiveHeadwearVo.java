package com.erban.admin.main.vo;

public class GiveHeadwearVo {
    private Long erbanNo;

    private Long uid;

    private String headwearId;

    private String headwearName;

    private Integer goldPrice;

    private Integer headwearDate;

    public Long getErbanNo() {
        return erbanNo;
    }

    public void setErbanNo(Long erbanNo) {
        this.erbanNo = erbanNo;
    }

    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public String getHeadwearId() {
        return headwearId;
    }

    public void setHeadwearId(String headwearId) {
        this.headwearId = headwearId;
    }

    public String getHeadwearName() {
        return headwearName;
    }

    public void setHeadwearName(String headwearName) {
        this.headwearName = headwearName;
    }

    public Integer getGoldPrice() {
        return goldPrice;
    }

    public void setGoldPrice(Integer goldPrice) {
        this.goldPrice = goldPrice;
    }

    public Integer getHeadwearDate() {
        return headwearDate;
    }

    public void setHeadwearDate(Integer headwearDate) {
        this.headwearDate = headwearDate;
    }
}
